package edu.rogerio.primeirasemana;

public class Operadores {
    
    public static void main(String[] args) {
        /*
         * = operador de atribuição
         * + operador de adição ou concatenação ou unario positivo
         * -  de subtração ou unario negativo
         * * operador de mutuiplicação
         * / operador de divisão
         * % operador de mododulo
         * -- decremento de valor
         * ++ operador de incremento
         * ! operador de negação
         */

         System.out.println(1+1+1+1);
         System.out.println(1+"1"+1+1);
         System.out.println("1"+1+1+1);
         System.out.println(1+1+1+"1");

         //operdores unario
         int numero = 5;
         numero = - numero;
         System.out.println(numero);                
    }
}
